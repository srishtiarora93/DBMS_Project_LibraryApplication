	IF ((24 * (to_date(to_char(roomRecordSet.enddate, 'MM-DD-YYYY hh24:mi'), 'MM-DD-YYYY hh24:mi') - to_date(roomRecordSet.startdate, 'MM-DD-YYYY hh24:mi'))) > 1) THEN
		IF ((roomRecordSet.checkoutdeadline < sysdate) AND (roomRecordSet.enddate > sysdate)) THEN
			IF (roomRecordSet.checkoutdatetime IS NULL) THEN
				UPDATE studyRoomReservation SET isvoid = 'Y'
				WHERE userid = roomRecordSet.userid
				AND roomno = roomRecordSet.roomno
				AND startdate = roomRecordSet.startdate
				AND enddate = roomRecordSet.enddate;
			END IF;
		END IF;
	END IF;
END LOOP;
COMMIT;
END PROC_ROOM_RESERVE_CANCEL;

