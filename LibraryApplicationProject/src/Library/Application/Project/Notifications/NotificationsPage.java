    private void detailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailButtonActionPerformed
        int selectedRow = messageNotificationTable.getSelectedRow();
        if(selectedRow == -1) return;
        String patronMessageNo = (String)messageNotificationTable.getModel().getValueAt(selectedRow, 0);
        String masterMessageId = (String)messageNotificationTable.getModel().getValueAt(selectedRow, 1);
        this.setVisible(false);
        new NotificationDetailsPage(this,patronMessageNo,masterMessageId).setVisible(true);
    }//GEN-LAST:event_detailButtonActionPerformed

   
