    private void CreateTableModel() {
        m_Model = new DefaultTableModel();
        m_Model.addColumn("Identifier");
        m_Model.addColumn("Book type");
        confTable.setModel(m_Model);
    }

    private void PopulateConfTable() {
        try {
            AddEcopyConfToTable();
            AddHardCopyConfToTable();
        }
        catch (Exception e) {
            Logger.getLogger(ConferencePage.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    private void AddEcopyConfToTable() throws Exception{
        CheckedoutResourcesHelper.AddBooksToTable("CONFNO", "USERID", 
                m_UserId, "CHECKSOUTECOPYCONF", "RDATETIME", PublicationType.Ecopy, m_Model);
    }

    private void AddHardCopyConfToTable() throws Exception{
        CheckedoutResourcesHelper.AddBooksToTable("CONFNO", "USERID", 
                m_UserId, "CHECKSOUTHARDCOPYCONF", "RDATETIME", PublicationType.Hardcopy, m_Model);
    }

    private String getTableNameFromBookType(String bookType) {
        if (bookType.equals(PublicationType.Ecopy.toString())){
            return "CHECKSOUTECOPYCONF";
        }
        return "CHECKSOUTHARDCOPYCONF";
    }

    private boolean getIsDueDatePresent(String bookType) {
        return !bookType.equals(PublicationType.Ecopy.toString());
    }
