    private void detailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailsButtonActionPerformed
        try {
            int selectedRow = confTable.getSelectedRow();
            if(selectedRow == -1){
                return;
            }
            String bookId = (String)confTable.getModel().getValueAt(selectedRow, 0);
            String bookType = (String)confTable.getModel().getValueAt(selectedRow, 1);
            String tableName = getTableNameFromBookType(bookType);
            boolean isDueDatePresent = getIsDueDatePresent(bookType);
            this.setVisible(false);
            new PublicationDetailsPage(this, m_UserId, 
                    bookId, tableName, "CONFNO", isDueDatePresent).setVisible(true);
        }
        catch (Exception e) {
            Logger.getLogger(ConferencePage.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_detailsButtonActionPerformed

