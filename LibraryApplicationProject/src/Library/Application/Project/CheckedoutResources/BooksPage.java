    private void CreateTableModel() {
        m_Model = new DefaultTableModel();
        m_Model.addColumn("Identifier");
        m_Model.addColumn("Book type");
        booksTable.setModel(m_Model);
    }

    private void PopulateBooksTable() {
        try {
            AddEcopyBooksToTable();
            AddHardCopyBooksToTable();
        }
        catch (Exception e) {
            Logger.getLogger(BooksPage.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    private void AddEcopyBooksToTable() throws Exception{
        CheckedoutResourcesHelper.AddBooksToTable("ISBN", "USERID", 
                m_UserId, "CHECKSOUTECOPYBOOK", "RDATETIME", PublicationType.Ecopy, m_Model);
    }

    private void AddHardCopyBooksToTable() throws Exception{
        CheckedoutResourcesHelper.AddBooksToTable("ISBN", "USERID", 
                m_UserId, "CHECKSOUTHARDCOPYBOOK", "RDATETIME", PublicationType.Hardcopy, m_Model);
    }

    private String getTableNameFromBookType(String bookType) {
        if (bookType.equals(PublicationType.Ecopy.toString())){
            return "CHECKSOUTECOPYBOOK";
        }
        return "CHECKSOUTHARDCOPYBOOK";
    }

    private boolean getIsDueDatePresent(String bookType) {
        return !bookType.equals(PublicationType.Ecopy.toString());
    }
