    private void detailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailsButtonActionPerformed
        try {
            int selectedRow = booksTable.getSelectedRow();
            if(selectedRow == -1){
                return;
            }
            String bookId = (String)booksTable.getModel().getValueAt(selectedRow, 0);
            String bookType = (String)booksTable.getModel().getValueAt(selectedRow, 1);
            String tableName = getTableNameFromBookType(bookType);
            boolean isDueDatePresent = getIsDueDatePresent(bookType);
            this.setVisible(false);
            new PublicationDetailsPage(this, m_UserId, 
                    bookId, tableName, "ISBN", isDueDatePresent).setVisible(true);
        }
        catch (Exception e) {
            Logger.getLogger(BooksPage.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_detailsButtonActionPerformed

