    private void CreateTableModel() {
        m_Model = new DefaultTableModel();
        m_Model.addColumn("Identifier");
        m_Model.addColumn("Book type");
        journalsTable.setModel(m_Model);
    }

    private void PopulateJournalsTable() {
        try {
            AddEcopyJournalsToTable();
            AddHardCopyJournalsToTable();
        }
        catch (Exception e) {
            Logger.getLogger(JournalsPage.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    private void AddEcopyJournalsToTable() throws Exception{
        CheckedoutResourcesHelper.AddBooksToTable("ISSN", "USERID", 
                m_UserId, "CHECKSOUTECOPYJOURNAL", "RDATETIME", PublicationType.Ecopy, m_Model);
    }

    private void AddHardCopyJournalsToTable() throws Exception{
        CheckedoutResourcesHelper.AddBooksToTable("ISSN", "USERID", 
                m_UserId, "CHECKSOUTHARDCOPYJOURNAL", "RDATETIME", PublicationType.Hardcopy, m_Model);
    }
    
    private String getTableNameFromBookType(String bookType) {
        if (bookType.equals(PublicationType.Ecopy.toString())){
            return "CHECKSOUTECOPYJOURNAL";
        }
        return "CHECKSOUTHARDCOPYJOURNAL";
    }

    private boolean getIsDueDatePresent(String bookType) {
        return !bookType.equals(PublicationType.Ecopy.toString());
    }

