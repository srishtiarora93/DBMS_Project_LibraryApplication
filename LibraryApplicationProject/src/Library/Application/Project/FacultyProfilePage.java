    private void PopulateFacultyProfilePage(String facultyNo)throws Exception {
     try(IDatabaseConnection connection = DatabaseConnectionService.createDatabaseConnection()){
            try(IStatementExecutor stmtExecutor = DatabaseConnectionService.createStatementExecutor(connection)){
                 String faculty = String.format("SELECT * FROM Faculty WHERE facultyNo ='%s' ",facultyNo);
                try (IQueryResultSet resultSet = stmtExecutor.executeQuery(faculty)){
                    resultSet.moveToFirstRow();
                    SetRowValuesToTextboxControls(resultSet);
                }
            }
        }
    }

    private void SetRowValuesToTextboxControls( final IQueryResultSet resultSet) {
        facultyNoTextBox.setText(resultSet.getString("facultyNo"));
        firstNameTextBox.setText(resultSet.getString("firstName"));
        lastNameTextBox.setText(resultSet.getString("lastName"));
        categoryTextBox.setText(resultSet.getString("category"));
        nationalityTextBox.setText(resultSet.getString("nationality"));
        PopulateDeptIdComboBox();
        deptIdComboBox.setSelectedItem(resultSet.getString("deptId"));
    }

    private void ChangeEditModeOfTextBoxes(boolean makeEditable) {
        categoryTextBox.setEditable(makeEditable);
        nationalityTextBox.setEditable(makeEditable);
        deptIdComboBox.setEditable(makeEditable);
    }

    private void PopulateDeptIdComboBox() {
        List<String> deptId;
        try {
            deptId = GetArrayListOfAllDeptIds();
            
            deptIdComboBox.setModel(new DefaultComboBoxModel(deptId.toArray()));
        } catch (Exception ex) {
            Logger.getLogger(FacultyProfilePage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void UpdateFacultyProfile(String m_facultyNo)throws Exception {
        try(IDatabaseConnection connection = DatabaseConnectionService.createDatabaseConnection()){
            try(IStatementExecutor stmtExecutor = DatabaseConnectionService.createStatementExecutor(connection)){
                String updateFacultyProfileInfo = String.format("UPDATE Faculty "
                       +"SET category = '%s',"
                       +"nationality = '%s',"
                       +"deptId = '%s'"
                       +"WHERE facultyNo = '%s'",
                        categoryTextBox.getText(),nationalityTextBox.getText(),deptIdComboBox.getSelectedItem(), m_facultyNo);
                            
                stmtExecutor.executeUpdate(updateFacultyProfileInfo);
            }
        }
    }

    private List<String> GetArrayListOfAllDeptIds()throws Exception {
        List<String> listOfDeptIds = new ArrayList<>();
        try(IDatabaseConnection connection = DatabaseConnectionService.createDatabaseConnection()){
            try(IStatementExecutor stmtExecutor = DatabaseConnectionService.createStatementExecutor(connection)){
                String deptIds = String.format("SELECT deptId FROM Departments ");
                try (IQueryResultSet resultSet = stmtExecutor.executeQuery(deptIds)){
                        while (resultSet.getResultSet().next()){
                        listOfDeptIds.add(resultSet.getString("deptId"));
                    }
                }
            }
        }
        return listOfDeptIds;
    }
