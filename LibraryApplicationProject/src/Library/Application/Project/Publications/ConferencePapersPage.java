    private void CreateTableModel() {
        m_Model = new DefaultTableModel();
        m_Model.addColumn("Identifier");
        m_Model.addColumn("Type");
        m_Model.addColumn("Title");
        conferencePapersTable.setModel(m_Model);
    }
    
    private void PopulateConferencePapersTable() {
        try {
            AddEcopyConfToTable();
            AddHardcopyConfToTable();
        }
        catch (Exception e) {
            Logger.getLogger(ConferencePapersPage.class.getName()).log(Level.SEVERE, null, e);
        }
    }
    
    private void AddEcopyConfToTable() throws Exception{
        PublicationsHelper.AddBooksToTable("confNo", "TITLE", "EcopyConf", PublicationType.Ecopy, m_Model);
    }

    private void AddHardcopyConfToTable() throws Exception{
        PublicationsHelper.AddBooksToTable("confNo", "TITLE", "HardcopyConf", PublicationType.Hardcopy, m_Model);
    }
    
    private String getTableName(String bookType) {
        if (bookType.equals(PublicationType.Ecopy.toString())) return "EcopyConf";
        return "HardcopyConf";
    }

